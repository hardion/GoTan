@implementation GotanDomain
@synthesize tableViewArray;

#pragma mark -
#pragma mark View lifecycle


- (void)viewDidLoad {
    [super viewDidLoad];

    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
	NSArray *array = [[NSArray alloc] initWithObjects:@"sys",@"Microsoft",@"Samsung",@"Motorola",nil];

	responseData = [[NSMutableData data] retain];
	NSURLRequest *request = [NSURLRequest requestWithURL:[NSURL URLWithString:@"http://localhost:8182/devices"]];
	[[NSURLConnection alloc] initWithRequest:request delegate:self];
	
	self.tableViewArray = array;
	[array release];
}

- (void)connection:(NSURLConnection *)connection didReceiveResponse:(NSURLResponse *)response {
	[responseData setLength:0];
}

- (void)connection:(NSURLConnection *)connection didReceiveData:(NSData *)data {
	[responseData appendData:data];
}

- (void)connection:(NSURLConnection *)connection didFailWithError:(NSError *)error {
//	NSString* textError = [NSString stringWithFormat:@"Connection failed: %@", [error description]]
//	self.tableViewArray = [[NSArray alloc] initWithObjects:textError,nil]
//	[textError release]
}

- (void)connectionDidFinishLoading:(NSURLConnection *)connection {
	[connection release];
	
	NSString *responseString = [[NSString alloc] initWithData:responseData encoding:NSUTF8StringEncoding];
	[responseData release];
	
	NSDictionary *dictionary = [responseString JSONValue];
	NSLog(@"Dictionary value for \"domain\" is \"%@\"", [dictionary objectForKey:@"domains"]);
	
	NSArray *array = [dictionary objectForKey:@"domains"];

	self.tableViewArray = array;
	[array release];
	
	[self.tableView reloadData];

}


